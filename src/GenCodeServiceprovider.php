<?php

namespace Gen\Code;

use Gen\Code\Console\Commands\GenCodeScript;
use Illuminate\Support\ServiceProvider;

class GenCodeServiceProvider extends ServiceProvider
{
    /**
     * Bootstrap the application services.
     *
     * @return void
     */
    public function boot()
    {
